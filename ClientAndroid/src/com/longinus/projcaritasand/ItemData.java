public class ItemData {
	public String name;
	public String type;
	public String weight;
	public String quantity;
	
	public ItemData(String name, String type, String weight, String quantity) {
		super();
		this.name = name;
		this.type = type;
		this.weight = weight;
		this.quantity = quantity;
	}
}
